package mobile.tiantai.android.bo;

import hy.plat.bo.BaseBean;

public class CusToCom implements BaseBean{
	private String ctcKey;
	private String ctcId;
	private String account;
	private String comId;
	private String comName;
	public String getCtcKey() {
		return ctcKey;
	}
	public void setCtcKey(String ctcKey) {
		this.ctcKey = ctcKey;
	}
	public String getCtcId() {
		return ctcId;
	}
	public void setCtcId(String ctcId) {
		this.ctcId = ctcId;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getComId() {
		return comId;
	}
	public void setComId(String comId) {
		this.comId = comId;
	}
	public String getComName() {
		return comName;
	}
	public void setComName(String comName) {
		this.comName = comName;
	}
	
	
}
